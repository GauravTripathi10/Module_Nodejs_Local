// Module.exports or exports is use  for exporting the functions or modules outside


module.exports.add=(a,b,c)=>{
    // This is a addition function
    return a+b+c;

}

module.exports.mutli=(a,b,c)=>{
    return a*b;
}

module.exports.maxNum=(a,b)=>{
    // This Function returns the maximum number 
    return Math.max(a,b);
}